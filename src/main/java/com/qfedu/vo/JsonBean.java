package com.qfedu.vo;

public class JsonBean {
	
	private Integer code;//成功或失败的状态1成功 0失败
	private Object msg;
	
	
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public Object getMsg() {
		return msg;
	}
	public void setMsg(Object msg) {
		this.msg = msg;
	}
	
	
	
}
