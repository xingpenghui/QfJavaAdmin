package com.qfedu.pojo;

public class RoleResource {

	
	private Integer id;
	private Role role;
	private Resources recources;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
	public Resources getRecources() {
		return recources;
	}
	public void setRecource(Resources recource) {
		this.recources = recource;
	}
	
	
	
}
